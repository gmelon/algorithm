package nadongbin.dp.n9;

import java.util.Scanner;

public class 병사_배치하기 {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int N = sc.nextInt(); // 병사의 수

        int[] powers = new int[N];
        for (int i = 0; i < N; i++) {
            powers[i] = sc.nextInt();
        }

        sc.close();

        // 풀이 시작


    }

}
